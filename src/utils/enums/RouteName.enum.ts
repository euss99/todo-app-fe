/**
 * Enum representing the application routes
 * Each value corresponds to a specific route path in the application
 */
enum RouteName {
  /** Home page route */
  HOME = "/",
  /** Login page route */
  LOGIN = "/login",
  /** Register page route */
  REGISTER = "/register",
  /** Counter page route */
  COUNTER = "/counter"
}

export default RouteName;